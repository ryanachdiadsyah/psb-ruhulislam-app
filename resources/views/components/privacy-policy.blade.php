<h1>Kebijakan Privasi</h1>

<div>
    Terakhir diperbarui: <strong>1 Januari 2026</strong>
</div>
<hr>


<h2>1. Pendahuluan</h2>
<p>
Kebijakan Privasi ini menjelaskan bagaimana <strong>Ruhul Islam Anak Bangsa</strong> mengumpulkan, menggunakan, menyimpan, dan melindungi data pribadi yang diperoleh melalui aplikasi Penerimaan Santri Baru (PSB) di domain <strong>psb.ruhulislam.com</strong> atau <strong>ruhulislam.com</strong>.
</p>

<p>
Dengan menggunakan aplikasi PSB, orang tua atau wali calon santri menyatakan telah membaca, memahami, dan menyetujui Kebijakan Privasi ini.
</p>

<h2>2. Jenis Data yang Dikumpulkan</h2>
<p>
Kami mengumpulkan data pribadi berikut:
</p>
<ul>
    <li>Identitas calon santri (nama, tempat/tanggal lahir, alamat, dll)</li>
    <li>Identitas orang tua atau wali</li>
    <li>Data kontak (nomor telepon, email)</li>
    <li>Dokumen pendukung (akta kelahiran, kartu keluarga, rapor, dan sejenisnya)</li>
    <li>Informasi transaksi pembayaran (tanpa menyimpan data kartu atau PIN)</li>
</ul>

<h2>3. Data Anak</h2>
<p>
Data calon santri yang dikumpulkan merupakan <strong>data pribadi anak</strong>. Oleh karena itu:
</p>
<ul>
    <li>Pengisian dan persetujuan dilakukan oleh orang tua atau wali yang sah</li>
    <li>Sekolah tidak memproses data anak tanpa persetujuan wali</li>
    <li>Data digunakan secara terbatas hanya untuk keperluan penerimaan santri</li>
</ul>

<h2>4. Tujuan Penggunaan Data</h2>
<p>
Data pribadi digunakan untuk:
</p>
<ul>
    <li>Memproses pendaftaran calon santri</li>
    <li>Verifikasi data dan dokumen</li>
    <li>Administrasi seleksi dan penerimaan santri</li>
    <li>Keperluan komunikasi resmi terkait PSB</li>
</ul>

<h2>5. Penyimpanan dan Keamanan Data</h2>
<p>
Kami menyimpan data pribadi selama diperlukan untuk proses penerimaan santri dan administrasi internal Sekolah.
</p>
<p>
Sekolah menerapkan langkah teknis dan organisatoris yang wajar untuk melindungi data dari akses tidak sah, kehilangan, atau penyalahgunaan.
</p>
<p>
Namun, Pengguna memahami bahwa tidak ada sistem elektronik yang sepenuhnya aman.
</p>

<h2>6. Pembagian Data kepada Pihak Ketiga</h2>
<p>
Kami tidak menjual atau menyewakan data pribadi kepada pihak lain.
</p>
<p>
Data dapat dibagikan secara terbatas kepada:
</p>
<ul>
    <li>Penyedia payment gateway untuk keperluan transaksi</li>
    <li>Pihak yang diwajibkan berdasarkan peraturan perundang-undangan</li>
</ul>

<h2>7. Payment Gateway</h2>
<p>
Pembayaran biaya pendaftaran dilakukan melalui penyedia payment gateway pihak ketiga. Sekolah tidak menyimpan data kartu, PIN, atau kredensial pembayaran.
</p>
<p>
Pengolahan data pembayaran tunduk pada kebijakan privasi penyedia payment gateway terkait.
</p>

<h2>8. Hak Orang Tua / Wali</h2>
<p>
Orang tua atau wali berhak untuk:
</p>
<ul>
    <li>Meminta akses terhadap data pribadi</li>
    <li>Meminta perbaikan data yang tidak akurat</li>
    <li>Meminta penghapusan data sesuai ketentuan hukum</li>
</ul>

<h2>9. Retensi dan Penghapusan Data</h2>
<p>
Data akan disimpan selama diperlukan untuk tujuan PSB dan dapat dihapus sesuai kebijakan internal Sekolah atau permintaan sah dari wali, sepanjang tidak bertentangan dengan kewajiban hukum.
</p>

<h2>10. Perubahan Kebijakan Privasi</h2>
<p>
Sekolah berhak mengubah Kebijakan Privasi ini sewaktu-waktu. Perubahan akan diumumkan melalui aplikasi PSB dan berlaku sejak tanggal pembaruan.
</p>

<h2>11. Hukum yang Berlaku</h2>
<p>
Kebijakan Privasi ini tunduk dan ditafsirkan berdasarkan hukum Republik Indonesia.
</p>

