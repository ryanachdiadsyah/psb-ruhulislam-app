<h2>2. Ruang Lingkup Layanan</h2>
<p>
Aplikasi PSB digunakan untuk:
</p>
<ul>
    <li>Pendaftaran calon santri</li>
    <li>Pengisian data pribadi calon santri dan orang tua/wali</li>
    <li>Unggah dokumen persyaratan administrasi</li>
    <li>Pembayaran biaya pendaftaran melalui payment gateway</li>
</ul>
<p>
Aplikasi ini bukan layanan pembelajaran dan bukan layanan keuangan.
</p>

<h2>3. Persetujuan Pengguna</h2>
<p>
Dengan menggunakan aplikasi PSB ini, orang tua atau wali calon santri menyatakan telah membaca, memahami, dan menyetujui seluruh Syarat dan Ketentuan ini, serta memberikan persetujuan sah atas pengolahan data pribadi anak.
</p>
<p>
Jika tidak menyetujui ketentuan ini, Pengguna dilarang menggunakan aplikasi PSB.
</p>

<h2>4. Data Pribadi</h2>
<p>
Sekolah mengumpulkan dan memproses data berikut:
</p>
<ul>
    <li>Identitas calon santri</li>
    <li>Identitas orang tua atau wali</li>
    <li>Dokumen pendukung (akta lahir, kartu keluarga, rapor, dan sejenisnya)</li>
    <li>Informasi transaksi pembayaran (tanpa menyimpan data kartu atau PIN)</li>
</ul>
<p>
Data digunakan hanya untuk keperluan proses penerimaan santri dan administrasi internal Sekolah.
</p>

<h2>5. Keamanan Data</h2>
<p>
Sekolah berupaya menerapkan langkah pengamanan teknis dan administratif yang wajar. Namun, Pengguna memahami bahwa tidak ada sistem elektronik yang sepenuhnya aman.
</p>
<p>
Sekolah tidak bertanggung jawab atas kebocoran data yang disebabkan oleh kelalaian pengguna atau serangan di luar kendali wajar sistem.
</p>

<h2>6. Unggah Dokumen</h2>
<p>
Pengguna bertanggung jawab penuh atas keaslian dan kebenaran dokumen yang diunggah. Sekolah berhak menolak atau membatalkan pendaftaran apabila ditemukan dokumen palsu atau tidak valid.
</p>

<h2>7. Pembayaran</h2>
<p>
Pembayaran dilakukan melalui payment gateway pihak ketiga. Sekolah tidak menyimpan data kartu, PIN, atau kredensial pembayaran lainnya.
</p>
<p>
Biaya pendaftaran bersifat <strong>tidak dapat dikembalikan (non-refundable)</strong> kecuali ditentukan lain secara tertulis oleh Sekolah.
</p>

<h2>8. Pembatalan dan Penolakan</h2>
<p>
Sekolah berhak menolak atau membatalkan proses pendaftaran apabila ditemukan pelanggaran, data tidak benar, atau penyalahgunaan sistem. Keputusan Sekolah bersifat final.
</p>

<h2>9. Batasan Tanggung Jawab</h2>
<p>
Sekolah tidak bertanggung jawab atas kerugian tidak langsung, kegagalan akses akibat gangguan internet pengguna, atau kesalahan input data oleh Pengguna.
</p>

